// Lógica de navegação do menu principal

let opcao = 1

switch (opcao) {
    case 1:
        console.log('Cadastrar');
        break;
    case 2:
        console.log('Listar');
        break;
    case 3:
        console.log('Sair');
        break;
    default:
        console.log('Opção inválida');
}
